bool isUpper(String word) {
  return word != word.toUpperCase();
}

Iterable<int> _constructRunes(String start, String end) sync* {
  int s = start.runes.first;
  int e = end == null ? s : end.runes.first;

  for(var i = s; i <= e; i++) {
    yield i;
  }
}

